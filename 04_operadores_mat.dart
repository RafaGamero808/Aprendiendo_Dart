main(){
  int a = 10 + 5;
  int b = 10 - 5;
  int c = 10 * 5;
  double d = 10 / 5;
  double 10 % 3; //Sacar el resto en una división
  b= -b;          //Usamos para cambiar el valor de la expresión

  print(a);
  print(b);
  print(c);
  print(d);

  int w = 1;
  w++;            //++ 2  / Incremento de 1 en 1
  w--;            //-- 1  / Disminución de 1 en 1

  w += 2;         // Incremento de 2 en 2
  w -= 2;         // Disminución de 2 en 2
  w *= 2;         // multiplicación en 2
  w /= 2;         // división en 2
}